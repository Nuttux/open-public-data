import { chromium } from 'playwright';
const browser = await chromium.launch({ headless: true });
const ctx = await browser.newContext({ viewport: { width: 1440, height: 900 }, deviceScaleFactor: 1 });
const p = await ctx.newPage();
await p.goto('file:///Users/daniel/code/open-public-data/mockups/finance/hero-1-cni-civique.html', { waitUntil: 'networkidle' });
await p.waitForTimeout(800);
await p.evaluate(() => window.scrollTo({ top: 2350, behavior: 'instant' }));
await p.waitForTimeout(300);
await p.screenshot({ path: '/tmp/finance-mockups/v3-retain-cards.png', clip: { x: 0, y: 0, width: 1440, height: 900 } });
await browser.close();
