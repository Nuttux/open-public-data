import { chromium } from 'playwright';
const browser = await chromium.launch({ headless: true });
const ctx = await browser.newContext({ viewport: { width: 1440, height: 900 }, deviceScaleFactor: 1 });
const p = await ctx.newPage();
await p.goto('http://localhost:3000/daily-bread?net=45000&parts=2&c=marseille', { waitUntil: 'networkidle', timeout: 30000 });
await p.waitForTimeout(1500);
await p.screenshot({ path: '/tmp/finance-mockups/PROD-daily-bread-fold.png', clip: { x:0, y:0, width:1440, height:900 } });
await p.screenshot({ path: '/tmp/finance-mockups/PROD-daily-bread-full.png', fullPage: true });
await browser.close();
console.log('done');
